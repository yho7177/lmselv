package kr.happyjob.study.lmg.model;

import java.sql.Date;
import java.sql.Timestamp;

/**
/**

  * @FileName : ClassInfo.java

  * @Project : lmselv

  * @Date : 2021. 11. 12. 

  * @작성자 : 윤효열

  * @변경이력 :

  * @프로그램 설명 : tb_classinfo ==> 강의 정보를 담고 있는 테이블

  */
public class ClassInfo {
	
	private int ci_no;
	private String loginID;
	private String ci_type;
	private String ci_name;
	private Timestamp ci_date;
	private Date ci_start;
	private Date ci_end;
	private String ci_goal;
	private String ci_attend;
	private String ci_plan;
	private String ci_limit;
	public ClassInfo() {
		super();
	}
	public ClassInfo(int ci_no, String loginID, String ci_type, String ci_name, Timestamp ci_date, Date ci_start,
			Date ci_end, String ci_goal, String ci_attend, String ci_plan, String ci_limit) {
		super();
		this.ci_no = ci_no;
		this.loginID = loginID;
		this.ci_type = ci_type;
		this.ci_name = ci_name;
		this.ci_date = ci_date;
		this.ci_start = ci_start;
		this.ci_end = ci_end;
		this.ci_goal = ci_goal;
		this.ci_attend = ci_attend;
		this.ci_plan = ci_plan;
		this.ci_limit = ci_limit;
	}
	public int getCi_no() {
		return ci_no;
	}
	public void setCi_no(int ci_no) {
		this.ci_no = ci_no;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public String getCi_type() {
		return ci_type;
	}
	public void setCi_type(String ci_type) {
		this.ci_type = ci_type;
	}
	public String getCi_name() {
		return ci_name;
	}
	public void setCi_name(String ci_name) {
		this.ci_name = ci_name;
	}
	public Timestamp getCi_date() {
		return ci_date;
	}
	public void setCi_date(Timestamp ci_date) {
		this.ci_date = ci_date;
	}
	public Date getCi_start() {
		return ci_start;
	}
	public void setCi_start(Date ci_start) {
		this.ci_start = ci_start;
	}
	public Date getCi_end() {
		return ci_end;
	}
	public void setCi_end(Date ci_end) {
		this.ci_end = ci_end;
	}
	public String getCi_goal() {
		return ci_goal;
	}
	public void setCi_goal(String ci_goal) {
		this.ci_goal = ci_goal;
	}
	public String getCi_attend() {
		return ci_attend;
	}
	public void setCi_attend(String ci_attend) {
		this.ci_attend = ci_attend;
	}
	public String getCi_plan() {
		return ci_plan;
	}
	public void setCi_plan(String ci_plan) {
		this.ci_plan = ci_plan;
	}
	public String getCi_limit() {
		return ci_limit;
	}
	public void setCi_limit(String ci_limit) {
		this.ci_limit = ci_limit;
	}
	
	@Override
	public String toString() {
		return "ClassInfo [ci_no=" + ci_no + ", loginID=" + loginID + ", ci_type=" + ci_type + ", ci_name=" + ci_name
				+ ", ci_date=" + ci_date + ", ci_start=" + ci_start + ", ci_end=" + ci_end + ", ci_goal=" + ci_goal
				+ ", ci_attend=" + ci_attend + ", ci_plan=" + ci_plan + ", ci_limit=" + ci_limit + "]";
	}
	
	
}
