package kr.happyjob.study.jmg.model;

public class ResuMgtModel {
	
	private int ci_no;
	private String ci_name;
	private String ci_start;
	private String ci_end;
	private int ci_limit;
	private String name;
	private String people;
	
	
	
	public String getPeople() {
		return people;
	}
	public void setPeople(String people) {
		this.people = people;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCi_no() {
		return ci_no;
	}
	public void setCi_no(int ci_no) {
		this.ci_no = ci_no;
	}
	public String getCi_name() {
		return ci_name;
	}
	public void setCi_name(String ci_name) {
		this.ci_name = ci_name;
	}
	public String getCi_start() {
		return ci_start;
	}
	public void setCi_start(String ci_start) {
		this.ci_start = ci_start;
	}
	public String getCi_end() {
		return ci_end;
	}
	public void setCi_end(String ci_end) {
		this.ci_end = ci_end;
	}
	public int getCi_limit() {
		return ci_limit;
	}
	public void setCi_limit(int ci_limit) {
		this.ci_limit = ci_limit;
	}

}
