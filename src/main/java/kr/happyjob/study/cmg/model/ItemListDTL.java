package kr.happyjob.study.cmg.model;

//장비정보
public class ItemListDTL {
   
	private int r_no;
	private int i_no;
	private String i_name;
	private int i_cnt;
	private String i_content;
	
	public int getR_no() {
		return r_no;
	}
	
	public void setR_no(int r_no) {
		this.r_no = r_no;
	}
	
	public int getI_no() {
		return i_no;
	}
	
	public void setI_no(int i_no) {
		this.i_no = i_no;
	}
	
	public String getI_name() {
		return i_name;
	}
	
	public void setI_name(String i_name) {
		this.i_name = i_name;
	}
	
	public int getI_cnt() {
		return i_cnt;
	}
	
	public void setI_cnt(int i_cnt) {
		this.i_cnt = i_cnt;
	}
	
	public String getI_content() {
		return i_content;
	}
	
	public void setI_content(String i_content) {
		this.i_content = i_content;
	}
	
	@Override
	public String toString() {
		return "ItemListDTL [r_no=" + r_no + ", i_no=" + i_no + ", i_name=" + i_name + ", i_cnt=" + i_cnt + ", i_content="
				+ i_content + "]";
	}
	
}
